# Comparions Operators
a, b = 7, 10
print(a == b)
print(a != b)
print(a < b)
print(a > b)

#Logical Operators
print((a == 7 and b == 10)) # both the conditions must be true
print((a == 1 or b == 10)) # one of the two conditions must be true
print(not(a == 7 or b == 10)) # opposite of the output returned by the condition