a = 10
b = 7

print(a + b) # add 2 numbers
print(a * b) # multiply 2 numbers
print(a - b) # substract 2 numbers
print(a / b) # divide 2 numbers
print(a // b) # divide 2 numbers but the output must be an integer
print(a % b) # returns remainder of the division
print(a ** b) # return exponential of the number

# Store the addition of 2 numbers in a
a += b
print(a)
# You can perform all the operations there +=, -=, *=, %=, /= any operator